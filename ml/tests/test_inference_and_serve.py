@pytest.fixture
def stub_app() -> TestClient:
    return TestClient(build_app(StubService()))


def _png_bytes(width: int = 32, height: int = 32) -> bytes:
    img = Image.new("RGB", (width, height), color=(120, 80, 200))
    buf = io.BytesIO()
    img.save(buf, format="PNG")
    return buf.getvalue()


def test_healthz_returns_metadata(stub_app: TestClient) -> None:
    resp = stub_app.get("/healthz")
    assert resp.status_code == 200
    body = resp.json()
    assert body["status"] == "ok"
    assert body["base_channels"] == 16


def test_suggest_from_image_round_trip(stub_app: TestClient) -> None:
    resp = stub_app.post(
        "/infer/suggest-from-image",
        files={"file": ("input.png", _png_bytes(), "image/png")},
    )
    assert resp.status_code == 200
    body = resp.json()
    assert body["family"] == "julia"
    assert body["recipe"]["fractalType"] == "julia"
    assert body["c_re"] == pytest.approx(-0.7)


def test_suggest_from_image_rejects_non_image(stub_app: TestClient) -> None:
    resp = stub_app.post(
        "/infer/suggest-from-image",
        files={"file": ("note.txt", b"not an image", "text/plain")},
    )
    assert resp.status_code == 415


def test_variations_endpoint(stub_app: TestClient) -> None:
    body = {
        "recipe": {
            "viewport": {"xMin": -1.5, "xMax": 1.5, "yMin": -1.5, "yMax": 1.5},
            "renderSettings": {"widthPx": 256, "heightPx": 256, "samplesPerAxis": 1},
            "colorSettings": {"paletteName": "fire", "mode": "linear"},
            "fractalType": "julia",
            "params": {
                "cRe": -0.7,
                "cIm": 0.27,
                "maxIter": 200,
                "escapeRadius": 2.0,
                "smoothing": True,
            },
        },
        "count": 4,
        "seed": 1,
    }
    resp = stub_app.post("/infer/variations", json=body)
    assert resp.status_code == 200
    recipes = resp.json()["recipes"]
    assert len(recipes) == 4
    for r in recipes:
        assert r["fractalType"] == "julia"


def test_variations_rejects_malformed_recipe(stub_app: TestClient) -> None:
    resp = stub_app.post("/infer/variations", json={"recipe": {}, "count": 2, "seed": 0})
    assert resp.status_code == 400
